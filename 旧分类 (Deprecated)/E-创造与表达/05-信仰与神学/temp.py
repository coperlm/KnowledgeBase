def create_kb_structure(base_path: Path, structure: dict):
    """
    递归创建目录结构并写入 index.md
    """
    for folder_name, folder_data in structure.items():
        # 1. 创建目录
        current_dir = base_path / folder_name
        current_dir.mkdir(parents=True, exist_ok=True)

        # 2. 准备 index.md 的内容
        title = folder_data.get("title", folder_name)
        desc = folder_data.get("desc", "暂无简介")

        # 拼接格式：第一行标题，第二行空行，第三行简介
        md_content = f"# {title}\n\n{desc}\n"

        # 3. 写入 index.md 文件
        index_file = current_dir / "index.md"
        with open(index_file, "w", encoding="utf-8") as f:
            f.write(md_content)

        # 4. 如果存在子目录，递归调用
        sub_dirs = folder_data.get("sub_dirs")
        if sub_dirs:
            create_kb_structure(current_dir, sub_dirs)

